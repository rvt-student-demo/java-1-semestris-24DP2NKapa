package rvt;

public class VariousVariable {
    public static void main(String[] args) {
        int chickenAmount = 9000;
        double bacon = 0.1;
        String tractorName = "Zetor";
        System.out.println("Chicken:");
        System.out.println(chickenAmount);
        System.out.println("Bacon (kg):");
        System.out.println(bacon);
        System.out.println("Tractor: \n" + tractorName);
        System.out.println("And finally, a summary:");
        System.out.println(chickenAmount + "\n" + bacon + "\n" + tractorName);
    }
}